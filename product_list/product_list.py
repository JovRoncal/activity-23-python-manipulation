# product_list.py

product_list = ["laptop", "mouse", "keyboard", "monitor", "printer", "headphones", "phone", "charger"]

# Accessing specific elements in the list
print(f"First product: {product_list[0]}")
print(f"Second product: {product_list[1]}")

# Slicing the list
print(f"First three products: {product_list[:3]}")
print(f"Last two products: {product_list[-2:]}")
